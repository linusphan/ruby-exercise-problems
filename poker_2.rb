class Card
  attr_reader :rank, :suit
  include Comparable

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(@rank, @rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

class Deck
  RANKS = (2..10).to_a + %w(Jack Queen King Ace).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    reset
  end

  def draw
    reset if @deck.empty?
    @deck.pop
  end

  private

  def reset
    @deck = RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end

    @deck.shuffle!
  end
end

class PokerHand
  def initialize(deck)
    @hand = []
    5.times {
      @hand << deck.draw
    }
  end

  def print
    puts *@hand
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    ['Ace', 'King', 'Queen', 'Jack', 10].each do |card|
      return false if !@hand.map(&:rank).include?(card)
    end
    @hand.uniq { |card| card.suit }.count == 1
  end

  def straight_flush?
    sorted_hand = @hand.map(&:value).sort
    sorted_hand.each_with_index do |num, index|
      unless index == @hand.size - 1
        return false if (sorted_hand[index + 1] - num) != 1
      end
    end
    @hand.uniq { |card| card.suit }.count == 1
  end

  def four_of_a_kind?
    @hand.map(&:value).each do |card|
      return true if @hand.map(&:value).count(card) == 4
    end
    false
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def flush?
    @hand.map(&:suit).uniq.count == 1
  end

  def straight?
    sorted_hand = @hand.map(&:value).sort
    sorted_hand.each_with_index do |num, index|
      unless index == @hand.size - 1
        return false if (sorted_hand[index + 1] - num) != 1
      end
    end
  end

  def three_of_a_kind?
    @hand.map(&:value).each do |card|
      return true if @hand.map(&:value).count(card) == 3
    end
    false
  end

  def two_pair?
    counter = 0
    uniq_cards = @hand.map(&:value).uniq
    uniq_cards.each do |card|
      counter += 1 if @hand.map(&:value).count(card) == 2
    end
    counter == 2
  end

  def pair?
    @hand.map(&:value).each do |card|
      return true if @hand.map(&:value).count(card) == 2
    end
    false
  end
end

